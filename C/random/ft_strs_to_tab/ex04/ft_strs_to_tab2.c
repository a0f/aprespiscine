#include <unistd.h>
#include <stdio.h>
#include <stdlib.h>
#include "ft_stock_str.h"

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (*(str + i) != '\0')
		i++;
	return (i);
}

char	*ft_strcpy(char *str)
{
	char	*dest;
	int	i;

	i = 0;
	while (str[i])
		i++;
	dest = (char *)malloc(sizeof(char) * (i + 1));
	i = 0;
	if (!(dest))
		return (NULL);
	while (str[i])
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

struct s_stock_str	*ft_strs_to_tab(int ac, char **av)
{
	t_stock_str	*stock;
	int		i;

	stock = (t_stock_str *)malloc(sizeof(t_stock_str) * (ac + 1));
	if (!stock)
		return (NULL);
	i = 0;
	while (i < ac)
	{
		stock[i].size = ft_strlen(av[i]);
		stock[i].str = av[i];
		stock[i].copy = ft_strcpy(av[i]);
		i++;
	}
	stock[i].size = 0;
	stock[i].str = NULL; // Set to NULL to indicate the end of the array
	stock[i].copy = NULL; // Set to NULL as well
	return (stock);
}

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

void	ft_putnum(int nb)
{
	long	i;

	i = nb;
	if (i < 0)
	{
		ft_putchar('-');
		i *= -1;
	}
	if (i > 9)
	{
		ft_putnum(i / 10);
		ft_putnum(i % 10);
	}
	else
		ft_putchar(i + 48);
}

void	ft_show_tab(struct s_stock_str *par)
{
	unsigned int	i;

	i = -1;
	while (par[++i].str != 0)
	{
		ft_putstr(par[i].str);
		ft_putchar('\n');
		ft_putnum(par[i].size);
		ft_putchar('\n');
		ft_putstr(par[i].copy);
		ft_putchar('\n');
	}
}

int main(int argc, char **argv)
{
    t_stock_str *stock;

    stock = ft_strs_to_tab(argc, argv);
    if (!stock)
    {
        // Handle memory allocation failure
        fprintf(stderr, "Memory allocation failed.\n");
        return 1;
    }

    ft_show_tab(stock);

    // Clean up: free memory
    for (int i = 0; i < argc; i++)
        free(stock[i].copy);
    free(stock);

    return 0;
}
